char	*ft_strcat(char *dest, char *src)
{
	int	len;

	len = 0;
	while (dest[len] != '\0')
		len++;
	while (*src != '\0')
	{
		dest[len] = *src;
		len++;
		src++;
	}
	dest[len] = '\0';
	return (dest);
}
