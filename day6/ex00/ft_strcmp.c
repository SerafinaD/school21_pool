int	ft_strcmp(char *s1, char *s2)
{
	unsigned char cs1;
	unsigned char cs2;

	while ((*s1 != '\0') || (*s2 != '\0'))
	{
		cs1 = *s1;
		cs2 = *s2;
		if (cs1 != cs2)
			return (cs1 - cs2);
		s1++;
		s2++;
	}
	return (0);
}
