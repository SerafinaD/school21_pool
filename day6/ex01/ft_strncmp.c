int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int cur;

	cur = 0;
	while (((s1[cur] != '\0') || (s2[cur] != '\0')) && (cur != n))
	{
		if (s1[cur] != s2[cur])
			return (s1[cur] - s2[cur]);
		cur++;
	}
	return (0);
}
